class Dog: # Isto é uma classe de objetos
    def ladrar(self): # Isto é um método (função de uma determinada classe de objetos)
        print("Ladrar!")

d = Dog() # Aqui fazemos com que o objeto "d" seja do tipo "Dog"

print(type(d)) # Aqui fazemos com que o programa nos retorne o tipo do objeto "d"

d.ladrar() # Aqui usamos o atributo "ladrar" do tipo de objeto "Dog"